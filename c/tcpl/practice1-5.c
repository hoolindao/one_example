/**
 * 逆序输出温度转换表
 * 即frah:300～0
 */

#include <stdio.h>

int main() {
    int farh;
    for(farh = 300; farh >= 0; farh = farh - 20)
        printf("%d\t%6.2f\n", farh, (5.0/9.0)*(farh-32));
    return 0;
}

